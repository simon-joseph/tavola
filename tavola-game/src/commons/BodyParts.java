package commons;

/**
 * Describes Snake's body part. If you consider a body part to be and arrow from
 * one side of a square to the other, then, for example, {@code DOWNRIGHT}
 * refers to an arrow from bottom side to right side.
 * 
 * @author sla
 * 
 */
public enum BodyParts {
    UPDOWN, LEFTDOWN, RIGHTDOWN, LEFTRIGHT, UPRIGHT, DOWNRIGHT, RIGHTLEFT, UPLEFT, DOWNLEFT, DOWNUP, LEFTUP, RIGHTUP;

}
